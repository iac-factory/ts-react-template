/*** Continuously mutate Flatten's prototype with a recursively updated Object constructor (`construct`) until flat */
const Flatten = ( object: object ) => {
    const construct = Object.prototype;

    for ( const [ key, value ] of Object.entries( object ) ) {
        if ( typeof value === "object" ) {
            Reflect.construct( Flatten, [ { ...construct, ...Flatten( value ) } ] );
        } else {
            construct[ key ] = value;
        }
    }

    return construct;
};

export const Colors = Flatten( Color.Variable() ) as Interface.Color;
